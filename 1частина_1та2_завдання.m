% Вхідні дані
P = [1.0  1.0  2.0  2.0;
     1.0 -1.0  0.0 -2.0];

% Цільові класи
T = [1 1 0 0];

% Створення персептрона
net = perceptron;

% Кількість епох навчання
net.trainParam.epochs = 20;

% Навчання
net = train(net, P, T);

% Отримання результатів класифікації
Y = net(P);

% Виведення таблиці з результатами
disp('--- Результати класифікації ---');
disp('     Вхідний вектор      Цільовий клас     Класифіковано як');
for i = 1:size(P, 2)
    fprintf('[%5.1f  %5.1f]             %d                %d\n', ...
        P(1, i), P(2, i), T(i), Y(i));
end

% Побудова графіка
figure;
plotpv(P, T);                % Точки за класами
plotpc(net.IW{1}, net.b{1}); % Розділяюча лінія
title('Одношаровий персептрон — Варіант 1');
legend('Клас 1', 'Клас 0', 'Лінія поділу');
grid on;